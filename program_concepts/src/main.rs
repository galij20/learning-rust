fn main() {
    //datatypes in rust
    //scalar
    let x : i32 = -4;
    println!("The value of x is {x}");
    //float
    let x : f32 = 3.9;
    println!("The value of x is {x}");
    //addition
    let sum = 5 + 20;
    //subtraction
    let difference : f64 = 8.5 - 3.2;
    //multiplication
    let product = 8 * 9;
    //division
    let quotient = 53.0 / 8.0;
    //remainder
    let remainder = 48 % 4;
    
    println!("Sum = {sum} \nDifference = {difference}");
    println!("Product = {product} \nQuotient = {quotient}");
    println!("Remainder = {remainder}");
    
}
